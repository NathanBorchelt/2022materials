import java.util.Scanner;
public class Calculations {
    public static void main(String[] args) {
        int m=2;
        int n=5;
       
        System.out.println(m + " + " + n + " = " + (m + n));
        System.out.println(m + " - " + n + " = " + (m - n));
        System.out.println(n + " - " + m + " = " + (n - m));
        System.out.println(m + " * " + n + " = " + (m * n));
        System.out.println(m + " / " + n + " = " + (m / n));
        System.out.println(n + " / " + m + " = " + (n / m));
        System.out.println(m + " % " + n + " = " + (m % n));
        System.out.println(n + " % " + m + " = " + (n % m));

    }
}
